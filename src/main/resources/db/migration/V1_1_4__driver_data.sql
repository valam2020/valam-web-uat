CREATE TABLE IF NOT EXISTS DRIVER_DETAILS (  
driver_id int NOT NULL AUTO_INCREMENT,
  first_name varchar(100) ,
  last_name varchar(100),
  email varchar(100) ,
  driver_status varchar(100),
  is_car_assigned boolean,
  ph_num varchar(20) ,
  dl_num varchar(30) ,
  address varchar(300) ,
  current_lat DECIMAL(15,10) ,
  current_lng DECIMAL(20,10) ,
 image_url varchar(300),
 sts_id int, 
  dispatcher_id int,
  created_date date,
  modified_date date,
  dateOfBirth date,
  car_no varchar(300),
PRIMARY KEY (driver_id),
FOREIGN KEY (dispatcher_id) REFERENCES dispatcher_details (dispatcher_id) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (sts_id) REFERENCES ride_status (sts_id) ON DELETE CASCADE ON UPDATE CASCADE
);




