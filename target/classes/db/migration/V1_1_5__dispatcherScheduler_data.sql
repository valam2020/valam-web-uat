CREATE TABLE IF NOT EXISTS DISPATCHER_SCHEDULER (  
SCHEDULER_ID int NOT NULL AUTO_INCREMENT,
BEGIN_TIME datetime,
END_TIME datetime,
DISPATCHER_ID int,
DRIVER_ID int,
CAR_ID int,
PRIMARY KEY (SCHEDULER_ID),
FOREIGN KEY (DISPATCHER_ID) REFERENCES DISPATCHER_DETAILS (DISPATCHER_ID) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (DRIVER_ID) REFERENCES DRIVER_DETAILS (DRIVER_ID) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (CAR_ID) REFERENCES CAR_DETAILS (CAR_ID) ON DELETE CASCADE ON UPDATE CASCADE
);

