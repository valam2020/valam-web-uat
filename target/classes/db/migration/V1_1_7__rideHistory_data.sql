CREATE TABLE IF NOT EXISTS RIDE_HISTORY (  
RIDE_ID int NOT NULL AUTO_INCREMENT,
FROM_ADDRESS varchar(100) NOT NULL,
TO_ADDRESS varchar(100) NOT NULL,
pickup_date datetime,
drop_date datetime,
DISTANCE float,
payment_total float,
payment_type varchar(20),
CAR_ID int,
DRIVER_ID int,
sts_id int,
USER_ID int,
DISPATCHER_ID int,
DROP_LAT DECIMAL(10,8) NOT NULL,
DROP_LNG DECIMAL(11,8) NOT NULL,
PICKUP_LAT DECIMAL(10,8) NOT NULL,
PICKUP_LNG DECIMAL(11,8) NOT NULL,
expiry_timer BIGINT,
otp varchar(4),
PRIMARY KEY (RIDE_ID),
FOREIGN KEY (CAR_ID) REFERENCES CAR_DETAILS (CAR_ID) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (DRIVER_ID) REFERENCES DRIVER_DETAILS (DRIVER_ID) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (USER_ID) REFERENCES USER_DETAILS (USER_ID) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (sts_id) REFERENCES ride_status (sts_id) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (DISPATCHER_ID) REFERENCES DISPATCHER_DETAILS (DISPATCHER_ID) ON DELETE CASCADE ON UPDATE CASCADE
);