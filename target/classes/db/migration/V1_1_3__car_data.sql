CREATE TABLE IF NOT EXISTS CAR_DETAILS (
  car_id int NOT NULL AUTO_INCREMENT,
  car_color varchar(100) NOT NULL,
  car_model varchar(100) NOT NULL,
  car_register_id varchar(100) NOT NULL UNIQUE,
  is_driver_assigned boolean,
  car_type varchar(100),
  comfort_level varchar(20) NOT NULL,
  image_url varchar(300),
  dispatcher_id int,
  sts_id int, 
  created_date date,
  modified_date date,
  PRIMARY KEY (car_id),
  FOREIGN KEY (dispatcher_id) REFERENCES DISPATCHER_DETAILS (Dispatcher_Id) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (sts_id) REFERENCES ride_status (sts_id) ON DELETE CASCADE ON UPDATE CASCADE
);



